/**
 * Mock Indian Land Registry Database
 * 
 * Contains 55 realistic land registry entries from multiple Indian states:
 * - Maharashtra: 7/12 extract format (Village Form VII/XII)
 * - Karnataka: RTC (Record of Rights, Tenancy and Crops) format
 * - Uttar Pradesh: Khatauni format
 * 
 * Each entry includes:
 * - Survey number in state-specific format
 * - Boundary polygon as [lat, lng] coordinate arrays forming closed polygons
 * - ULPIN (Unique Land Parcel Identification Number) — 14-digit Bhu-Aadhar format
 * - Realistic ownership, land use, and encumbrance data
 * 
 * Polygons are clustered around real Indian cities:
 * - Bangalore (Bengaluru): ~12.97°N, 77.59°E
 * - Pune: ~18.52°N, 73.86°E
 * - Lucknow: ~26.85°N, 80.95°E
 */

export interface LandRegistryEntry {
  /** Unique survey number in state-specific format */
  surveyNumber: string;
  /** State name */
  state: string;
  /** District name */
  district: string;
  /** Taluk/Tehsil name */
  taluk: string;
  /** Village name */
  village: string;
  /** Registered owner name */
  ownerName: string;
  /** Area in hectares */
  area: number;
  /** Boundary polygon as array of [latitude, longitude] coordinates forming a closed polygon */
  boundaryPolygon: [number, number][];
  /** Land use classification */
  landUse: 'residential' | 'commercial' | 'industrial' | 'agricultural' | 'mixed' | 'institutional';
  /** Legal encumbrances or disputes on the land */
  encumbrances: string[];
  /** Date of registration (ISO format) */
  registrationDate: string;
  /** ULPIN — 14-digit Bhu-Aadhar unique land parcel ID */
  ulpin: string;
}

/**
 * Helper: Generate a rectangular polygon around a center point.
 * Creates a realistic parcel boundary with slight irregularity.
 * 
 * @param centerLat - Center latitude
 * @param centerLng - Center longitude
 * @param widthDeg - Width in degrees (approx 111km per degree at equator)
 * @param heightDeg - Height in degrees
 * @param skew - Optional skew factor for irregularity
 */
function makePolygon(
  centerLat: number,
  centerLng: number,
  widthDeg: number,
  heightDeg: number,
  skew: number = 0
): [number, number][] {
  const hw = widthDeg / 2;
  const hh = heightDeg / 2;
  // Create a slightly irregular quadrilateral (5 points — closed polygon)
  return [
    [centerLat - hh + skew * 0.0001, centerLng - hw],
    [centerLat - hh, centerLng + hw + skew * 0.00005],
    [centerLat + hh + skew * 0.00008, centerLng + hw],
    [centerLat + hh, centerLng - hw - skew * 0.00003],
    [centerLat - hh + skew * 0.0001, centerLng - hw], // close the polygon
  ];
}

